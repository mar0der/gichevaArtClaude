SET NAMES utf8mb4;
DROP TABLE IF EXISTS `artwork_images`;
DROP TABLE IF EXISTS `artworks`;
CREATE TABLE `artworks` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `slug` VARCHAR(80) NOT NULL UNIQUE,
  `title` VARCHAR(255) NOT NULL,
  `description` TEXT,
  `price_usd` DECIMAL(10,2) DEFAULT NULL,
  `medium` VARCHAR(100) DEFAULT NULL,
  `length_cm` DECIMAL(6,2) DEFAULT NULL,
  `width_cm` DECIMAL(6,2) DEFAULT NULL,
  `thickness_cm` DECIMAL(6,2) DEFAULT NULL,
  `has_frame` TINYINT(1) NOT NULL DEFAULT 0,
  `availability` ENUM('not_for_sale','for_sale','sold') NOT NULL DEFAULT 'for_sale',
  `year_created` SMALLINT DEFAULT NULL,
  `saachi_link` VARCHAR(512) DEFAULT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE `artwork_images` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `artwork_id` INT NOT NULL,
  `image_name` VARCHAR(120) NOT NULL,
  `display_order` INT NOT NULL DEFAULT 0,
  `alt_text` VARCHAR(255) DEFAULT NULL,
  FOREIGN KEY (`artwork_id`) REFERENCES `artworks`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

